function M.setup()
  local status_ok, alpha = pcall(require, "alpha")
  if not status_ok then
    return
  end

  math.randomseed(os.time())
  local logo = logos[math.random(#logos)]

  local function pick_color()
    local colors = { "String", "Identifier", "Keyword", "Number" }
    return colors[math.random(#colors)]
  end

  local dashboard = require "alpha.themes.dashboard"
  dashboard.section.header.val = vim.split(one_piece_2, "\n")
  dashboard.section.header.opts.hl = pick_color()
  dashboard.section.buttons.val = {
    dashboard.button("r", "‚å®Ô∏è  Recently used files", ":Telescope oldfiles <CR>"),
    dashboard.button("f", "üÖïÔúù  Find file", ":Telescope find_files <CR>"),
    dashboard.button("t", "üÖïÔûÉ  Find word", ":Telescope live_grep <CR>"),
    dashboard.button("e", "ÔÖõ  New file", ":ene <BAR> startinsert <CR>"),
    dashboard.button("c", "Óòï  Configuration", ":e $MYVIMRC<CR>"),
    dashboard.button("P", "Ôê•  Open Personal dotfiles", ":Telescope file_browser path=" .. dot_path .. "<CR>" ),
    dashboard.button("p", "ÔÅª  Find project", ":Telescope file_browser path=" .. projects_path .. "<CR>"),
    dashboard.button("q", "‚úòÔôô  Quit Neovim", ":qa<CR>"),
  }

  local function footer()
    -- Number of plugins
    local total_plugins = #vim.tbl_keys(packer_plugins)
    local datetime = os.date "%d-%m-%Y %H:%M:%S"
    local plugins_text = "  Ôòö "
      .. total_plugins
      .. " plugins"
      .. "  Ôîß v"
      .. vim.version().major
      .. "."
      .. vim.version().minor
